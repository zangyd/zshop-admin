<template>
  <div class="category-control" @click="visible = true">
    <slot name="control"/>

    <el-dialog
      width="600px"
      :visible.sync="visible"
      :append-to-body="true"
      @open="handleOpen">

      <el-tree
        v-if="visible"
        node-key="goods_category_id"
        :data="treeData"
        :props="treeProps"
        :default-checked-keys="checkedKeys"
        :close-on-click-modal="false"
        :show-checkbox="true"
        style="margin-top: -15px;"
        ref="tree"/>

      <div slot="footer" class="dialog-footer">
        <el-button
          @click="visible = false"
          size="small">取消</el-button>

        <el-button
          @click="handleConfirm"
          type="primary"
          size="small">确定</el-button>
      </div>
    </el-dialog>
  </div>
</template>

<script>
import util from '@/utils/util'
import { getGoodsCategoryList } from '@/api/goods/category'

export default {
  name: 'category-all',
  props: {
    // 选中节点
    checkedKeys: {
      type: Array,
      required: false,
      default: () => []
    }
  },
  data() {
    return {
      visible: false,
      treeData: [],
      treeProps: {
        label: 'name',
        children: 'children'
      }
    }
  },
  methods: {
    // 打开对话框
    handleOpen() {
      if (this.treeData.length > 0) {
        return
      }

      getGoodsCategoryList(null)
        .then(res => {
          this.treeData = util.formatDataToTree(res.data, 'goods_category_id')
        })
    },
    // 确认提交
    handleConfirm() {
      // 向父级组件返回数据
      if (this.$refs.tree) {
        this.$emit('confirm', this.$refs.tree.getCheckedKeys())
      }
    }
  }
}
</script>

<style scoped>
  .category-control {
    width: auto;
    display: inline;
  }
</style>
